function SysCheck () {
    if (Cursor.x == 7 && Cursor.y == 90) {
        Error(1, "UIE", "MC", true)
    }
    if (Cursor.x == 55 && Cursor.y == 68) {
        Error(2, "UIE", "MC", true)
    }
    SysCheckCycles += 1
}
